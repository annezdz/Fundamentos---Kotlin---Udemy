fun main() {
    println("2 + 2 =  ${2 + 2}")
    println("2 - 2 =  ${2 - 2}")
    println("2 / 2 =  ${2 / 2}")
    println("2 * 2 =  ${2 * 2}")
    println("10 % 4 = ${10 % 4}")
    println("10 % 2 = ${10 % 2}")

    var numero = 10
    println("numero++ = ${numero++}") // incrementa depois
    println("numero++ = ${++numero}") // incrementa antes

    println("numero++ = ${numero--}")
    println("numero++ = ${--numero}")
}